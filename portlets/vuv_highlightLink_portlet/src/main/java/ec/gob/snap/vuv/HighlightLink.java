
package ec.gob.snap.vuv;

/**
 * <p>Class utility to encapsulate info.</p> <b>Project:</b><p>VUV</p>
 * 
 * @version 1.0, 27/5/2015.
 */
public class HighlightLink {

	/**
	 * Attribute that represents the Highlight link category.
	 */
	private String category;
	/**
	 * Attribute that represents the preview text of the link.
	 */
	private String preview;
	/**
	 * Attribute that represents the full link description.
	 */
	private String detail;
	/**
	 * Attribute that represents the url.
	 */
	private String link;

	/**
	 * Constructor method for the class {@link HighlightLink}.
	 * 
	 * @param paramCategory
	 *            the link category.
	 * @param paramPreview
	 *            the preview of the link.
	 * @param paramDetail
	 *            the description of the link.
	 * @param paramLink
	 *            the url.
	 */
	public HighlightLink(
		String paramCategory, String paramPreview, String paramDetail,
		String paramLink) {

		super();
		category = paramCategory;
		preview = paramPreview;
		detail = paramDetail;
		link = paramLink;
	}

	/**
	 * Getter for category.
	 * 
	 * @return the Higlightlink category.
	 */
	public String getCategory() {

		return category;
	}

	/**
	 * Setter for category.
	 * 
	 * @param paramCategory
	 *            the link category.
	 */
	public void setCategory(String paramCategory) {

		category = paramCategory;
	}

	/**
	 * Getter for detail.
	 * 
	 * @return the Higlightlink detail.
	 */
	public String getDetail() {

		return detail;
	}

	/**
	 * Setter for detail.
	 * 
	 * @param paramPreview
	 *            the Higlightlink detail.
	 */
	public void setDetail(String paramPreview) {

		detail = paramPreview;
	}

	/**
	 * Getter for link.
	 * 
	 * @return the Higlightlink url.
	 */
	public String getLink() {

		return link;
	}

	/**
	 * Setter for link.
	 * 
	 * @param paramLink
	 *            the Higlightlink url.
	 */
	public void setLink(String paramLink) {

		link = paramLink;
	}

	/**
	 * Getter for preview.
	 * 
	 * @return the Higlightlink preview text.
	 */
	public String getPreview() {

		return preview;
	}

	/**
	 * Setter for preview.
	 * 
	 * @param paramDetail
	 *            the Higlightlink preview text.
	 */
	public void setPreview(String paramDetail) {

		preview = paramDetail;
	}

}
